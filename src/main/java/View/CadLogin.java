    private void botaoentrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoentrarActionPerformed

        
        try {
            String usuario, senha;

            usuario = txtusuario.getText();
            senha = txtsenha.getText();

            Login objlogin = new Login();
            objlogin.setUsuario(usuario);
            objlogin.setSenha(senha);

            LoginDao objlogindao = new LoginDao();
            ResultSet rslogindao = objlogindao.autenticaLogin(objlogin);

            if (rslogindao.next()) {
                Menu objmenu = new Menu();
                objmenu.setVisible(true);

                dispose();

            } else {
                JOptionPane.showMessageDialog(null, "Usuario ou Senha Inv√°lida");
            }

        } catch (SQLException erro) {
            JOptionPane.showMessageDialog(null, "Menu" + erro);

        } catch (IOException ex) {
            Logger.getLogger(CadLogin.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_botaoentrarActionPerformed
