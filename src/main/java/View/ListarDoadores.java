    public void atualizarDoadores(){
        DefaultTableModel mp1 = (DefaultTableModel) jtbl.getModel();
        
        int l = mp1.getRowCount();
        
        if(l>0){
            while(l>0){
                ((DefaultTableModel) jtbl.getModel()).removeRow(l-1);
                l--;
            }
        }
        
        try{
            ResultSet rs = control.TodosDoadores();
            
            DefaultTableModel mp = (DefaultTableModel) jtbl.getModel();
            
            while(rs.next()){
                String Coluna0=rs.getString("idDoador").toString().trim();
                String Coluna1=rs.getString("nomeDoador").toString().trim();
                String Coluna2=rs.getString("telefone").toString().trim();
                String Coluna3=rs.getString("cpf").toString().trim();
                String Coluna4=rs.getString("genero").toString().trim();
                mp.addRow(new String[] {Coluna0,Coluna1,Coluna2,Coluna3,Coluna4});
            
        }
       }
        catch (SQLException ex) {
                   JOptionPane.showMessageDialog(this, "erro:"+ex);
        }
        jtbl.setAutoCreateRowSorter(true);
    }

