    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        // TODO add your handling code here:
        if(jtbl.getSelectedRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Selecione um Doador para Excluir", "Atenção", JOptionPane.ERROR_MESSAGE );
        }
        else{
            int row = jtbl.getSelectedRow();
            int codigo = Integer.parseInt((String)jtbl.getValueAt(row,0));
            String nome = (String)jtbl.getValueAt(row,1);
            
            if (JOptionPane.showConfirmDialog(null, "Deseja Excluir o "+nome+"?","EXCLUIR", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                //System.out.println(codigo + " " + nome);
                control.ExcluirDoador(codigo);
                JOptionPane.showMessageDialog(null, nome+" Excluído com Sucesso!", "EXCLUIR", JOptionPane.WARNING_MESSAGE);
                atualizarDoadores();
            }
            else{
                JOptionPane.showMessageDialog(null, nome+" Não Excluído!", "EXCLUIR" ,JOptionPane.WARNING_MESSAGE);
            }
            
        }
    }//GEN-LAST:event_jBExcluirActionPerformed

